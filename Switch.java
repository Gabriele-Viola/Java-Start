public class Switch {
    // You can use a 'switch' statement because, in this case, using 'if' may make
    // your code too long
    public static void main(String[] args) {
        String nome = "Anna";
        switch (nome) {
            case "Giovanni":
                System.out.println("Ciao Giovanni");
                break;
            case "Anna":
                System.out.println("Ciao Anna");
                break;
            case "Paolo":
                System.out.println("Ciao Paolo");
                break;
            case "Elena":
                System.out.println("Ciao Elena");
                break;
            case "Gianni":
                System.out.println("Ciao Gianni");
                break;
        }
    }
}
